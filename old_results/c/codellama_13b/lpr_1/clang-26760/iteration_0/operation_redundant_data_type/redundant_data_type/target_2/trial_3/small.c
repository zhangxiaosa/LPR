
#include <stdio.h>

int main() {
    int i, j, l, m, n, o, p, k;
    unsigned char f, g;

    f = 0;
    g = 0;
    j = 0x24F96B7B;
    k = 0;

    while (f < j) {
        f++;
    }

    l = 1;
    m = j;
    n = g;
    o = n;
    p = o;

    while (i) {
        k = k >> l;
        i = p;
        if (i) {
            k = k << m;
        } else {
            break;
        }
    }

    return 0;
}
