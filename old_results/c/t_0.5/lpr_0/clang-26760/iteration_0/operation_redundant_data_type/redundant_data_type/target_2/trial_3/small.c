#include <stdio.h>

typedef signed a;
typedef int c;
typedef unsigned d;
typedef unsigned e;
d f;
a g;

int main() {
  a i;
  c j = 0x24F96B7BL;
  e k;
  
  for (f = 0; f != 0;) {
    for (; j != 0; --j)
      ;
  }
  
  c l = 1L;
  c m = j;
  a n = g;
  c o = n;
  e p = o;
  
  while (k != 0) {
    k = k >> l;
    i = (a)p;
    
    if (i != 0) {
      k = k << m;
    } else {
      break;
    }
  }
  
  return 0;
}