#include <stdint.h>

int main() {
  int f = 0;
  int j = 1518018315; // Hexadecimal value 0x5A73BC4B
  unsigned int k;

  for (; f;) {
    --j;
  }

  int l = 1;
  int m = j;
  int n = 0;
  int o = n;
  unsigned int p = o;

q:
  k = k >> l;
  int i = p;

  if (i) {
    k = k << m;
    goto q;
  }

  return 0;
}