#include <stdio.h>

signed fn1(signed p1, signed p2) { return p2 == 0 ? p1 : p1 % p2; }

unsigned m;
unsigned n;
signed o;

int main() {
  for (; n < 12; n = (n && 3 && n > 2147483647 - 3 || n < 0 && 3 < 0 && n) ? n : n + 3) {
    int ab = fn1(o, o);
    if (o <= 0xE3L)
      ;
    else if (ab)
      break;
  }
  return m;
}