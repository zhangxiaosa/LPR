int p;
int u;
int w;
int ac = 1;
int main() {
  int am = 1;
  int an;

  // Loop unrolling
  w = 3;

  // Iteration 1
  if (an = 250) {
    u = ac || (an = w);
    short ao = 0xBCD1;
    if ((ao - (am - an - 0xECL)) - u & 1)
      p = 1;
  }
  w--;

  // Iteration 2
  if (an = 250) {
    u = ac || (an = w);
    short ao = 0xBCD1;
    if ((ao - (am - an - 0xECL)) - u & 1)
      p = 1;
  }
  w--;

  // Iteration 3
  if (an = 250) {
    u = ac || (an = w);
    short ao = 0xBCD1;
    if ((ao - (am - an - 0xECL)) - u & 1)
      p = 1;
  }
  w--;

  printf("checksum = %d\n", p);
  return 0;
}