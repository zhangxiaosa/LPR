#include <stdio.h>

int main() {
  int l;
  unsigned n;
  int o;
  static unsigned p = 0x01B28DB7L;

  for (; n < 12; n = n + 3) {
    int ab;
    if (0x4F92 < (p | (ab = o % o)))
      ;
    else {
      int ac = 0xE3;
      if (o <= ac)
        ;
      else if (ab)
        break;
    }
  }
  return l;
}