
#include <stdio.h>

typedef unsigned int c;
typedef unsigned int d;
typedef unsigned int e;
d f;
int g;

int main() {
  c j = 0x24F96B7BL;
  e k;
  for (f = 0; f;)
    for (; j; --j)
      ;
  c l = 1L;
  c m = j;
  int n = g;
  c o = n;
  e p = o;
  while (p) {
    k = (unsigned int)k >> l;
    i = p;
    if (i) {
      k = k << m;
    } else {
      k = 0;
    }
  }
}
