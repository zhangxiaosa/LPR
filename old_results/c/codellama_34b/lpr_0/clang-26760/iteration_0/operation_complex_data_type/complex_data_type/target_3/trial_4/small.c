
#include <stdio.h>

typedef unsigned c;
typedef unsigned d;
typedef unsigned e;
d f;
int g;
int main() {
  int i;
  c j = 0x24F96B7BL;
  e k;
  for (f = 0; f;)
    for (; j; --j)
      ;
  c l = 1L;
  c m = j;
  int n = g;
  c o = n;
  e p = o;
  int k_old = k;
  int k_new = 0;
  while (p) {
    k_new = k_old >> l;
    i = p;
    if (i) {
      k_new = k_old << m;
    } else {
      k_new = 0;
    }
    k_old = k_new;
  }
}
