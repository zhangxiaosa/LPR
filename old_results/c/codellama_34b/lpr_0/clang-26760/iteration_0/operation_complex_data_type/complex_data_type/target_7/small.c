
#include <stdio.h>

unsigned int f;
int g;
int main() {
  int i;
  unsigned int j = 0x24F96B7BL;
  unsigned int k;
  for (f = 0; f;)
    for (; j; --j)
      ;
  unsigned int l = 1L;
  unsigned int m = j;
  int n = g;
  int o = n;
  int p = o;
  while (p) {
    k = (unsigned int)k >> l;
    i = p;
    if (i) {
      k = k << m;
    } else {
      k = 0;
    }
  }
}
