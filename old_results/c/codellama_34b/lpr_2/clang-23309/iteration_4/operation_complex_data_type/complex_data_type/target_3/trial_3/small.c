
// Variables with primary data types
int an;
short w;
unsigned ac;
int t;

// Function to compute the value of `ao`
int fn2(int p1, int p2) {
  return p1 - p2;
}

int main() {
  // Initialize variables
  an = 0x294A;
  w = 0;
  ac = 1;
  t = ac || (an = w);

  // Compute the value of `ao`
  int ao = fn2(ao - (1 - an), t);

  // Print the value of `ao`
  printf("checksum = %d\n", ao);

  return 0;
}
