int subtract(int p1, int p2) { return p1 - p2; }

int p;
int u;
int w;
int ac = 1;
int main() {
  int am = 1;
  int an;
  for (w = 3; w; w -= 1)
    if (an = 250) {
      u = ac;
      short ao = 0xBCD1;
      if ((ao - (subtract(am, 250) - 0xECL)) - u & 1)
        p = 1;
    }
  printf("checksum = %d\n", p);
  return 0;
}