#include <iostream>

unsigned m;
int o;

int main() {
  unsigned loopCount = m;
  for (; loopCount < 12; ) {
    int temp = o == 0 ? o : o % o;
    if (o <= 0xE3L)
      ;
    else if (temp)
      break;

    if (loopCount && 3 && loopCount > 2147483647 - 3)
      loopCount = loopCount;
    else if (loopCount < 0 && 3 < 0 && loopCount)
      loopCount = loopCount;
    else
      loopCount = loopCount + 3;
  }
}