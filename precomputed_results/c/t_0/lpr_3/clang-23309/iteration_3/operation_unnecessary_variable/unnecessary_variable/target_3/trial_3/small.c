#include <stdio.h>

unsigned p;
long v;
short w;
unsigned ab;
short ac = 1L;

long fn3(long p1, long p2) {
  return p1 - p2;
}

int main() {
  unsigned an;

  for (w = 3; w; w -= 1) {
    if (an = 250U) {
      ab = ac;
      short ao = 0xBCD1L;

      if (fn3(ao, ao - an) - ab & 1U)
        v = 1UL;
    }
  }

  p = v;
  printf("checksum = %X\n", p);
  return 0;
}