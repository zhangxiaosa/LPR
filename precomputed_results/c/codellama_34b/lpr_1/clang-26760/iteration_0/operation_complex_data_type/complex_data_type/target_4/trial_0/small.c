
#include <stdio.h>

// Decompose the complex data type into primary data types
typedef unsigned int e;
e f;
int g;
int main() {
  int i;
  int j = 0x24F96B7BL;
  e k;

  // Decompose the loop into primary data types
  for (f = 0; f;) {
    for (; j; --j) {
      ;
    }
  }

  int n = g;
  q:;
  k = k >> 1L;
  i = n;
  if (i) {
    k = k << j;
    goto q;
  }
}
