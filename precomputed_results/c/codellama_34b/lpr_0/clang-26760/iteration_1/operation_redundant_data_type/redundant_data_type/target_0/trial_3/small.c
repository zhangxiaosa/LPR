
#include <iostream>
#include <cstdint>

int32_t f;
int32_t g;
int32_t i;
int32_t n;
uint32_t j;
uint32_t k;

int main() {
    f = 0;
    g = 0;
    i = 0;
    n = 0;
    j = 0x24F96B7BL;
    k = 0;
    while (f) {
        for (; j; --j) {
        }
        n = g;
        while (n) {
            k = k >> 1L;
            i = n;
            if (i) {
                k = k << j;
            }
        }
    }
}
