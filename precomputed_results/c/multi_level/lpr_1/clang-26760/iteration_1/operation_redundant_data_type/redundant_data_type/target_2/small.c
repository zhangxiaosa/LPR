#include <stdio.h>

typedef unsigned e;
unsigned f;
int g;

int main() {
  int i;
  int j = 0x24F96B7BL;
  e k;

  for (f = 0; f;)
    for (; j; --j)
      ;

  int l = 1L;
  int m = j;
  int n = g;
  int o = n;
  e p = o;

  while (p) {
    k = k >> l;
    i = p;

    if (i)
      k = k << m;
  }
}