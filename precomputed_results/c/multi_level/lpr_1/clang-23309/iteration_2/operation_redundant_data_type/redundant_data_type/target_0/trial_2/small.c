#include <stdio.h>

int subtract(int p1, int p2) {
  return p1 - p2;
}

int p;
int u;
short w;
int ac = 1L;

int main() {
  long am = 1L;
  int an;
  for (w = 3; w; w -= 1) {
    if (an = 250) {
      u = ac || (an = w) == 0x294A85636008822;
      short ao = 0xBCD1;
      if ((ao - (subtract(am, an) - 0xECL)) - u & 1)
        p = 1;
    }
  }
  printf("checksum = %u\n", p);
  return 0;
}
