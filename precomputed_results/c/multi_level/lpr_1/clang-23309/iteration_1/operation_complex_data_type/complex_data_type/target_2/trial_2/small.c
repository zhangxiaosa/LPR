#include<stdio.h>

unsigned fn6(unsigned p1, unsigned p2) {
  return p1 - p2;
}

unsigned p;
unsigned u;
long v;
short w;
short ac = 1;

int main() {
  long am = 1;
  int an;

  for (w = 3; w; w -= 1) {
    if (an = 250) {
      u = ac || (an = w);
      short ao = 0xBCD1;

      if ((ao - (fn6(am, an) - 0xEC)) - u & 1) {
        v = 1;
      }
    }
  }

  p = v;
  printf("checksum = %u\n", p);

  return 0;
}
