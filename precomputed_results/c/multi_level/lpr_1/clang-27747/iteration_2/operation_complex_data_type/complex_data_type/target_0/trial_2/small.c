struct c {
  unsigned int d_i: 18;
  unsigned int e_i;
  unsigned int f_i;
  int g_i;
  int h_i;
};

struct c i;
struct c j;

int main() {
  struct c l = {24, 6738, 165};
  
  // Decomposed variables assignment
  d_l = j.e_i;
  n = j.h_i && d_l;
  o = d_l;
  
  // Expression assignment
  i.g_i = o | -(l.f_i && d_l) - n;

  return 0;
}