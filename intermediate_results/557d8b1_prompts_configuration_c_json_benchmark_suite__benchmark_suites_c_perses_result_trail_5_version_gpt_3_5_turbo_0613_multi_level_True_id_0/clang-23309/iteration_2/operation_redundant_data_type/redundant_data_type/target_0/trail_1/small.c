#include <stdio.h>

int fn1(int p1, int p2) { return p1 - p2; }

int fn2(int p1, int p2) { return p1; }

int f;
int g;
int a = 1;

int main() {
  int c;
  for (g = 3; g; g -= 1) {
    if (c = 250UL) {
      a || (c = g);
      int d = 0xBCD1;
      if (fn1(d, fn2(1 - c, 0xECL)) - a & 1UL)
        f = 1UL;
    }
  }
  printf("checksum = %X\n", f);
  return 0;
}
